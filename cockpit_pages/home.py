st.markdown("""
Bienvenue dans cette application pédagogique conçue pour explorer et démontrer les capacités des **Large Language Models (LLM)** à travers un cas d'usage fil rouge ludique : **l'univers Marvel**.

### 🏗️ Philosophie du Projet
Ce projet est conçu pour être **didactique** et **progressif**. L'objectif est de démystifier l'IA Générative :
*   **Approche Pas-à-Pas** : Nous partons d'un simple appel API pour aboutir à une architecture d'entreprise complexe utilisant des agents autonomes et le protocole MCP.
*   **Aesthetics First** : Les interfaces visent un rendu premium pour une expérience utilisateur moderne.
*   **Parallèle Entreprise** : Chaque concept illustré dans l'univers Marvel est suivi d'une "Ouverture SI" expliquant son équivalent dans un Système d'Information réel.

👈 **Utilisez le menu de gauche pour naviguer à travers les différentes phases et étapes de la démonstration.**
""")

